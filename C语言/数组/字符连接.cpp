//从键盘输入两个字符串，将第二个字符串连接到第一个字符串的后面，并输出结果。不能用字符串函数strcat()

#include <stdio.h>
void main ()
{
	 int i=0,j=0;
	 char s1[80],s2[80];
	 printf ("请输入第1个字符串：");
	 gets(s1);
	 printf ("请输入第2个字符串：");
	 gets(s2);
	 while (s1[i]!='\0')				//计算字符1的长度
		 i++;
	 while (s2[j]!='\0')				//注意斜杠不能少
	 {
		 s1[i]=s2[j];					//连接在1的末尾
		 i++;
		 j++;
	 }
	 s1[i]='\0';						//添加字符结束标志
	 printf ("连接后的字符为：%s\n",s1);
}